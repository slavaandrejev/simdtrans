    .global simdtrans

    .text

    # Load and transpose 2 rows of 2x2 submatrices (eight 2x2 blocks in total)
    # of 16x16 matrix of bytes. Store the result in two ymm registers.
    #     rfrom:  GPR with the address of the first row of the matrix to
    #             transpose.
    #     offset: Offset to the first row to start transposing from.
    #     regto1: ymm register to store rows 0, 2 of the transposed blocks
    #     regto2: ymm register to store rows 1, 3 of the transposed blocks
    #
    # \regto1 and \regto2 will have the following transposed 2x2 blocks:
    #  bytes: 31            24 23           16 15            8 7              0
    # \regto1 в”Ңв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”җ
    #         в”Ӯ1,7в”Ӯ1,6в”Ӯ1,5в”Ӯ1,4в”Ӯ1,3в”Ӯ1,2в”Ӯ1,1в”Ӯ1,0в•‘0,7в”Ӯ0,6в”Ӯ0,5в”Ӯ0,4в”Ӯ0,3в”Ӯ0,2в”Ӯ0,1в”Ӯ0,0в”Ӯ
    # \regto2 в””в”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”ҳ
    # Numbers above are block indices, i.e. the following arrangement of the
    # elements:
    # bytes:   31           30   17        16 15          14   1           0
    #          в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
    # \regto1: в”Ӯa[3,14]a[2,14]в”ӮвҖҰ|a[3,0]a[2,0]в•‘a[1,14]a[0,14]в”ӮвҖҰ|a[1,0]a[0,0]|
    # \regto2: в”Ӯa[3,15]a[2,15]в”ӮвҖҰ|a[3,1]a[2,1]в•‘a[1,15]a[0,15]в”ӮвҖҰ|a[1,1]a[0,1]|
    #          в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
    .macro transpose2x2x1 rfrom:req, offset:req, regto1:req, regto2:req
        vmovdqa      \offset(\rfrom), %ymm2
        vmovdqa      \offset + 0x20(\rfrom), %ymm1

        vinserti128  $1, %xmm1, %ymm2, %ymm0
        vextracti128 $1, %ymm2, %xmm2
        vinserti128  $0, %xmm2, %ymm1, %ymm1
        # ymm0 and ymm1 now have 4 rows arranged in pairs like this:
        # bytes: 31          24 23         16 15          8 7            0
        #        в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm0:  в”Ӯ           row 2           в•‘           row 0           в”Ӯ
        #        в”ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•«в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Ө
        # ymm1:  в”Ӯ           row 3           в•‘           row 1           в”Ӯ
        #        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ

        vpunpcklbw   %ymm1, %ymm0, %ymm2
        # ymm2 now has rows of transposed 2x2 blocks (0,0), (0,1), (0,2), (0,3),
        # (1,0), (1,1), (1,2), (1,3):
        # bytes: 31         30   17        16 15        14   1           0
        #        в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm2:  в”Ӯa[3,7]a[2,7]в”ӮвҖҰв”Ӯa[3,0]a[2,0]в•‘a[1,7]a[0,7]в”ӮвҖҰв”Ӯa[1,0]a[0,0]|
        #        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
        # We have to put odd words in one register, even in another to form
        # matrices.

        vpunpckhbw   %ymm1, %ymm0, %ymm3
        # ymm3 now has rows of transposed 2x2 blocks (0,4), (0,5), (0,6), (0,7),
        # (1,4), (1,5), (1,6), (1,7):
        # bytes: 31           30   17        16 15          14   1           0
        #        в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm3:  в”Ӯa[3,15]a[2,15]в”ӮвҖҰв”Ӯa[3,8]a[2,8]в•‘a[1,15]a[0,15]в”ӮвҖҰв”Ӯa[1,8]a[0,8]|
        #        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
        # We have to put odd words in one register, even in another to form
        # matrices.

        # separating even words (upper parts of 2x2 blocks) and packing them to
        # \regto1
        vpxor        %ymm4, %ymm4, %ymm4
        vpblendw     $0b01010101, %ymm2, %ymm4, %ymm4
        # ymm4 has the upper parts of blocks (0,0), (0,1), (0,2), (0,3), (1,0),
        # (1,1), (1,2), (1,3):
        # bytes: 31             28   19            16 15            12   3               0
        #        в”Ңв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm4:  |0,0|a[3,6]a[2,6]в”ӮвҖҰв”Ӯ0,0в”Ӯa[3,0]a[2,0]в•‘0,0|a[1,6]a[0,6]в”ӮвҖҰв”Ӯ0,0в”Ӯa[1,0]a[0,0]|
        #        в””в”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
        vpxor        %ymm5, %ymm5, %ymm5
        vpblendw     $0b01010101, %ymm3, %ymm5, %ymm5
        # ymm5 has the upper parts of blocks (0,4), (0,5), (0,6), (0,7),
        # (1,4), (1,5), (1,6), (1,7):
        # bytes: 31               28   19            16 15              12   3               0
        #        в”Ңв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm5:  |0,0|a[3,14]a[2,14]в”ӮвҖҰв”Ӯ0,0в”Ӯa[3,8]a[2,8]в•‘0,0|a[1,14]a[0,14]в”ӮвҖҰв”Ӯ0,0в”Ӯa[1,8]a[0,8]|
        #        в””в”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ

        # pack all upper parts to one register:
        vpackusdw    %ymm5, %ymm4, \regto1

        # separating odd words (lower parts of 2x2 blocks) and packing them to
        # \regto2
        vpsrldq      $2, %ymm2, %ymm2
        vpsrldq      $2, %ymm3, %ymm3
        vpblendw     $0b01010101, %ymm2, %ymm4, %ymm4
        # ymm4 has the lower parts of blocks (0,0), (0,1), (0,2), (0,3), (1,0),
        # (1,1), (1,2), (1,3):
        # bytes: 31             28   19            16 15            12   3               0
        #        в”Ңв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm4:  |0,0|a[3,7]a[2,7]в”ӮвҖҰв”Ӯ0,0в”Ӯa[3,1]a[2,1]в•‘0,0|a[1,7]a[0,7]в”ӮвҖҰв”Ӯ0,0в”Ӯa[1,1]a[0,1]|
        #        в””в”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
        vpblendw     $0b01010101, %ymm3, %ymm5, %ymm5
        # ymm5 has the lower parts of blocks (0,4), (0,5), (0,6), (0,7),
        # (1,4), (1,5), (1,6), (1,7):
        # bytes: 31               28   19            16 15              12   3               0
        #        в”Ңв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ҙв”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”¬в”Җв”¬в”Җв”Җв”Җв”¬в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
        # ymm5:  |0,0|a[3,15]a[2,15]в”ӮвҖҰв”Ӯ0,0в”Ӯa[3,9]a[2,9]в•‘0,0|a[1,15]a[0,15]в”ӮвҖҰв”Ӯ0,0в”Ӯa[1,9]a[0,9]|
        #        в””в”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв•Ёв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҙв”Җв”ҙв”Җв”Җв”Җв”ҙв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ

        # pack all lower parts to one register:
        vpackusdw    %ymm5, %ymm4, \regto2
    .endm

    # Swap high 128 bits of one ymm register with low 128 bits of another
    .macro swaphldqw r1N:req, r2N:req
        vextracti128 $1, %ymm\r1N, %xmm0
        vinserti128  $1, %xmm\r2N, %ymm\r1N, %ymm\r1N
        vinserti128  $0, %xmm0, %ymm\r2N, %ymm\r2N
    .endm

    # Transform 2x4 submatrices by transposing pairs of adjacent bytes as if
    # they represent elements of a 2x2 matrix. For example:
    #      в”Ӯ a[0,0] a[0,1] a[0,2] a[0,3] в”Ӯ    в”Ӯ a[0,0] a[0,1] a[1,0] a[1,1] в”Ӯ
    # from в”Ӯ a[1,0] a[1,1] a[1,2] a[1,3] в”Ӯ to в”Ӯ a[0,2] a[0,3] a[1,2] a[1,3] в”Ӯ.
    # \r1 and \r2 are ymm registers with 8 of such matrices. The result will be
    # in place.
    .macro transpose2x2x2 r1:req, r2:req
        # The algorithm is the same as in transpose2x2x1, except: (i) the elemet
        # size is not a byte, but a word; (ii) there is no pack instruction in
        # AVX2 that converts QWORD do DWORD, we have to use a couple of shuffles
        # instead.

        # get rows of transposed matrices in ymm0 and ymm1
        vpunpcklwd   \r2, \r1, %ymm0
        vpunpckhwd   \r2, \r1, %ymm1

        # separating even dwords
        vpxor        %ymm2, %ymm2, %ymm2
        vpblendw     $0b00110011, %ymm0, %ymm2, %ymm2
        vpxor        %ymm3, %ymm3, %ymm3
        vpblendw     $0b00110011, %ymm1, %ymm3, %ymm3
        vpshufd      $0b11011000, %ymm2, %ymm2
        vpshufd      $0b10001101, %ymm3, %ymm3
        vpor         %ymm2, %ymm3, \r1

        # separating odd dwords
        vpsrldq      $4, %ymm0, %ymm0
        vpsrldq      $4, %ymm1, %ymm1
        vpxor        %ymm2, %ymm2, %ymm2
        vpblendw     $0b00110011, %ymm0, %ymm2, %ymm2
